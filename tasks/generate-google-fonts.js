#!/usr/bin/env node

const fs = require("node:fs")
const path = require("node:path")

const API_ENDPOINT = "https://www.googleapis.com/webfonts/v1/webfonts"
const LOCAL_ENV_FILES = [".env", ".env.local"]
const OUTPUT_PATH = path.resolve("src/config/generated/google-fonts.ts")
const RECOMMENDED_GOOGLE_FONT_FAMILIES = new Set([
  "Noto Sans Arabic",
  "Noto Naskh Arabic",
  "Vazirmatn",
  "Cairo",
  "IBM Plex Sans Arabic",
  "Readex Pro",
  "Tajawal",
  "Almarai",
  "Amiri",
  "Scheherazade New",
  "Markazi Text",
  "Lalezar",
  "Noto Nastaliq Urdu",
  "Inter",
  "Roboto",
  "Open Sans",
  "Lato",
  "Montserrat",
  "Poppins",
  "Nunito",
  "Source Sans 3",
  "Work Sans",
  "Manrope",
  "DM Sans",
  "Rubik",
  "Merriweather",
  "Lora",
  "Playfair Display",
  "Noto Serif",
  "IBM Plex Serif",
  "JetBrains Mono",
  "Fira Code",
  "Source Code Pro",
  "Roboto Mono",
  "Pacifico",
  "Dancing Script"
])
const VALID_CATEGORIES = new Set([
  "display",
  "handwriting",
  "monospace",
  "sans-serif",
  "serif"
])

function getAPIKey() {
  const apiKey = process.env.GOOGLE_FONTS_API_KEY?.trim()
  if (!apiKey) {
    throw new Error(
      "GOOGLE_FONTS_API_KEY is required to generate the Google Fonts catalog. Set it in your shell, CI secret, or a gitignored .env.local file."
    )
  }

  return apiKey
}

function unquoteEnvValue(value) {
  const trimmedValue = value.trim()
  const quote = trimmedValue[0]
  if (
    (quote === '"' || quote === "'") &&
    trimmedValue[trimmedValue.length - 1] === quote
  ) {
    return trimmedValue.slice(1, -1)
  }

  return trimmedValue
}

async function loadLocalEnvFiles() {
  for (const fileName of LOCAL_ENV_FILES) {
    const filePath = path.resolve(fileName)
    let fileText = ""

    try {
      fileText = await fs.promises.readFile(filePath, "utf8")
    } catch (error) {
      if (error?.code === "ENOENT") continue
      throw error
    }

    for (const line of fileText.split(/\r?\n/)) {
      const trimmedLine = line.trim()
      if (!trimmedLine || trimmedLine.startsWith("#")) continue

      const separatorIndex = trimmedLine.indexOf("=")
      if (separatorIndex <= 0) continue

      const key = trimmedLine.slice(0, separatorIndex).trim()
      const value = unquoteEnvValue(trimmedLine.slice(separatorIndex + 1))
      if (!/^[A-Z_][A-Z0-9_]*$/.test(key)) continue
      if (process.env[key] === undefined) {
        process.env[key] = value
      }
    }
  }
}

function getFallbackForCategory(category) {
  switch (category) {
    case "handwriting":
      return "cursive"
    case "monospace":
      return "monospace"
    case "serif":
      return "serif"
    default:
      return "sans-serif"
  }
}

function uniqueSortedStrings(values) {
  return [...new Set(values.filter((value) => typeof value === "string"))].sort(
    (first, second) =>
      first.localeCompare(second, "en", {
        sensitivity: "base"
      })
  )
}

function normalizeAxes(axes) {
  if (!Array.isArray(axes)) return undefined

  const normalizedAxes = axes
    .map((axis) => {
      if (
        !axis ||
        typeof axis.tag !== "string" ||
        typeof axis.start !== "number" ||
        typeof axis.end !== "number"
      ) {
        return null
      }

      return {
        tag: axis.tag,
        start: axis.start,
        end: axis.end
      }
    })
    .filter(Boolean)
    .sort((first, second) => first.tag.localeCompare(second.tag, "en"))

  return normalizedAxes.length > 0 ? normalizedAxes : undefined
}

function normalizeFontItem(item) {
  if (!item || typeof item.family !== "string") return null

  const family = item.family.trim()
  const category = VALID_CATEGORIES.has(item.category)
    ? item.category
    : "sans-serif"
  if (!family) return null

  const normalized = {
    family,
    category,
    fallback: getFallbackForCategory(category),
    recommended: RECOMMENDED_GOOGLE_FONT_FAMILIES.has(family),
    subsets: uniqueSortedStrings(item.subsets || []),
    variants: uniqueSortedStrings(item.variants || [])
  }
  const axes = normalizeAxes(item.axes)
  if (axes) {
    normalized.axes = axes
  }

  return normalized
}

function buildGeneratedSource(fonts) {
  return `// Generated by tasks/generate-google-fonts.js from Google Fonts Developer API v1.
// Do not edit this file manually.

import type { GoogleFontMetadata } from "../google-fonts"

export const GOOGLE_FONTS_CATALOG_SOURCE = "google-fonts-developer-api-v1"

export const GENERATED_GOOGLE_FONTS = ${JSON.stringify(fonts, null, 2)} satisfies GoogleFontMetadata[]
`
}

async function main() {
  await loadLocalEnvFiles()
  const apiKey = getAPIKey()
  const url = new URL(API_ENDPOINT)
  url.searchParams.set("key", apiKey)
  url.searchParams.set("sort", "alpha")

  const response = await fetch(url)
  if (!response.ok) {
    throw new Error(
      `Google Fonts Developer API failed with ${response.status}.`
    )
  }

  const payload = await response.json()
  if (!payload || !Array.isArray(payload.items)) {
    throw new Error("Google Fonts Developer API returned an invalid payload.")
  }

  const fonts = payload.items
    .map(normalizeFontItem)
    .filter(Boolean)
    .sort((first, second) =>
      first.family.localeCompare(second.family, "en", {
        sensitivity: "base"
      })
    )

  if (fonts.length === 0) {
    throw new Error("Google Fonts Developer API returned an empty catalog.")
  }

  await fs.promises.mkdir(path.dirname(OUTPUT_PATH), { recursive: true })
  await fs.promises.writeFile(OUTPUT_PATH, buildGeneratedSource(fonts))
  console.log(`Generated ${fonts.length} Google Fonts.`)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
